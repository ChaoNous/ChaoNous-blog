// Script to extract SVG data from @iconify-json packages
// Run: node scripts/extract-svelte-icons.js

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = resolve(__dirname, '..');

// All icon names used in Svelte components (that use @iconify/svelte)
const iconNames = [
  // LightDarkSwitch
  'material-symbols:wb-sunny-outline-rounded',
  'material-symbols:dark-mode-outline-rounded',
  // Search
  'material-symbols:search',
  'fa7-solid:chevron-right',
  // MobileTOC
  'material-symbols:format-list-bulleted',
  'material-symbols:close',
  'material-symbols:article-outline',
  'mdi:pin',
  // WallpaperSwitch
  'material-symbols:image-outline',
  'material-symbols:wallpaper',
  'material-symbols:hide-image-outline',
  // MusicPlayer
  'material-symbols:error',
  'material-symbols:music-note',
  'material-symbols:pause',
  'material-symbols:play-arrow',
  'material-symbols:visibility-off',
  'material-symbols:expand-less',
  'material-symbols:shuffle',
  'material-symbols:skip-previous',
  'material-symbols:skip-next',
  'material-symbols:repeat-one',
  'material-symbols:repeat',
  'material-symbols:volume-off',
  'material-symbols:volume-down',
  'material-symbols:volume-up',
  'material-symbols:expand-more',
  'material-symbols:queue-music',
  'material-symbols:graphic-eq',
  // DisplaySettings
  'fa7-solid:arrow-rotate-left',
  // SharePoster
  'material-symbols:check',
  'material-symbols:link',
  'material-symbols:download',
];

// Map prefix to @iconify-json package name
const prefixToPackage = {
  'material-symbols': '@iconify-json/material-symbols',
  'fa7-solid': '@iconify-json/fa7-solid',
  'mdi': '@iconify-json/mdi',
};

// Load icon sets
const iconSets = {};
for (const [prefix, pkg] of Object.entries(prefixToPackage)) {
  try {
    const jsonPath = resolve(rootDir, 'node_modules', pkg, 'icons.json');
    const data = JSON.parse(readFileSync(jsonPath, 'utf-8'));
    iconSets[prefix] = data;
    console.log(`Loaded ${prefix}: ${Object.keys(data.icons).length} icons`);
  } catch (e) {
    console.error(`Failed to load ${pkg}:`, e.message);
  }
}

// Extract SVG data
const entries = [];
const missing = [];

for (const fullName of iconNames) {
  const [prefix, name] = fullName.split(':');
  const iconSet = iconSets[prefix];
  
  if (!iconSet) {
    missing.push(fullName);
    continue;
  }
  
  const iconData = iconSet.icons[name];
  if (!iconData) {
    missing.push(fullName);
    continue;
  }
  
  const width = iconData.width || iconSet.width || 24;
  const height = iconData.height || iconSet.height || 24;
  const body = iconData.body;
  
  entries.push({
    name: fullName,
    body,
    width,
    height,
  });
}

if (missing.length > 0) {
  console.error('\nMissing icons:', missing);
}

// Generate TypeScript file
let output = `// Auto-generated SVG icon data for Svelte components
// Generated by: node scripts/extract-svelte-icons.js
// This eliminates runtime API calls to api.iconify.design

export interface IconData {
  body: string;
  width: number;
  height: number;
}

export const ICON_SVG_DATA: Record<string, IconData> = {\n`;

for (const entry of entries) {
  output += `  "${entry.name}": {\n`;
  output += `    body: \`${entry.body}\`,\n`;
  output += `    width: ${entry.width},\n`;
  output += `    height: ${entry.height},\n`;
  output += `  },\n`;
}

output += `};\n`;

const outputPath = resolve(rootDir, 'src', 'utils', 'svelte-icon-svgs.ts');
writeFileSync(outputPath, output, 'utf-8');
console.log(`\nGenerated ${outputPath} with ${entries.length} icons`);
